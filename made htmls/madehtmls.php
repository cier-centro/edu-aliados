<!-- El compañamiento -->

<p>La estrategia de acompañamiento busca generar las condiciones para el desarrollo de un trabajo pedagógico entre pares que analice fortalezas y oportunidades de cualificación institucional, en desarrollo de acciones específicas para el apoyo pedagógico entre un colegio mentor y una institución educativa acompañada.</p>
<p>El acompañamiento <a href="#" data-toggle="tooltip" data-placement="bottom"  title="Instituciones Educativas Mentoras (características) Estas instituciones cuentan con resultados del nivel superior en las pruebas de Estado, al tiempo que son reconocidas por la cualificación de sus comunidades educativas de manera integral, unos currículos fortalecidos y proyectos trasversales que cuentan con la participación de diferentes actores educativos. Adicionalmente, son portadoras de una capacidad inigualable de establecer comunidades de aprendizaje en torno al mejoramiento del sistema educativo nacional.">Colegio mentor</a> – <a href="#" data-toggle="tooltip" data-placement="bottom"  title="Instituciones educativas Acompañadas (Características): Estas instituciones cuentan con niveles de desarrollo en la prueba de Estado que les remite a pensar con detenimiento sus procesos de formación, al punto que están dispuestas y abiertas a la interlocución con las instituciones mentoras. Son organizaciones escolares que escuchan, interiorizan y aportan desde su realidad y su contexto al mejoramiento de sus propias dinámicas de formación y de construcción de comunidad.">IE acompañada</a> se enmarca en un intercambio de conocimientos, experiencias y buenas prácticas para promover aprendizajes en doble vía: los dos colegios aprenden, se fortalecen, participan en un proceso de evaluación formativa y de esta manera, se esperan resultados que favorezcan el mejoramiento institucional en los dos establecimientos. <p>
<h4>Los colegios mentores</h4>
<p>De manera previa a especificar el rol de los colegios mentores, es importante resaltar el sentido del calificativo mentor, puesto que es relevante para este proceso. Mentor implica realizar una gestión que va más allá de un simple tutor, puesto que el mentor asesora, en este caso, en procesos de gestión pedagógica, con base en la experiencia y conocimientos que le han permitido alcanzar resultados de alto desempeño. La gestión del mentor no se queda solamente en asesorar, sino que trasciende hasta el punto de posibilitar nuevas oportunidades para su acompañado, para que sea factible concretar en el contexto real lo aprendido. En este sentido, es importante que el mentor tenga disposición para comunicar y compartir saberes y experiencias, habilidad para llegar a otras personas y competencias para el ejercicio de liderazgo, características que deben ser reconocidas por el acompañado.</p>
<h4>Las IE acompañadas </h4>
<p>son establecimientos educativos que han demandado acompañamiento como resultado del proceso de autoevaluación institucional mediante el cual han reconocido oportunidades para el mejoramiento y los ha lleva a tomar consciencia de la necesidad de una intervención externa. Es condición que estas IE tengan una matrícula superior a 500 estudiantes, hayan obtenido puntuación en un rango medio de la desviación estándar del promedio de la Prueba Saber 11 2015, a las cuales le fueron evaluados mínimo 15 estudiantes en dichas pruebas. Es importante que sea honesta en el análisis de los resultados de los aprendizajes de sus estudiantes y en el reconocimiento de las acciones desarrolladas y las que no han desarrollado, y que manifiesten voluntariamente su interés y voluntad para ser acompañadas por un colegio mentor par.</p>
