<h3>¿Qué es Aliados 10?</h3>
<h4>¿De dónde surge el Proyecto Aliados 10?</h4>
<p>El Proyecto surge del interés por generar un trabajo colaborativo y colectivo entre las instituciones educativas de la nación, iniciando con la creación de binas de trabajo que permitan compartir sus experiencias exitosas en educación que permitan el mejoramiento de aspectos directivos, académicos, pedagógicos y comunitarios.</p>
<p>Se trata entonces de aunar esfuerzos pedagógicos, didácticos, y administrativos mediante la construcción de planes de acompañamiento que se desarrollen entre las parejas de instituciones educativas.</p>
<p>Así mismo, se plantea recuperar el recorrido por algunas experiencias en la implementación de estrategias de acompañamiento previas, a fin de potenciar el trabajo que algunos colegios han reconocido como importante, esto mediante la potenciación de líderes académicos que apoyen el desarrollo entre pares.</p>
<p>Entre estas posibilidades se resaltan el desarrollo de capacidades internas de las escuelas, la expansión y diversificación de las oportunidades de aprendizaje, el manejo eficiente de recursos y el fomento del <a href="http://cambridge.academia.edu/MarianoRosenzvaig.">liderazgo pedagógico.</a></p>
<h4>¿Cuál es el propósito de Aliados 10?</h4>
<p>Consolidar una red de trabajo entre las instituciones mentoras y acompañadas para la mejora de los procesos de formación en la educación básica y media, manifestado en la obtención de mejores resultados de las pruebas de Estado y en la cualificación de la comunidad educativa. Esto implica que directivos, docentes, administrativos, estudiantes y padres de familia puedan participar de Aliados 10, haciendo un especial énfasis en la función directiva y en la docencia.</p>
<p>Este propósito cuenta con los siguientes objetivos:</p>
<ul>
	<li>Facilitar el intercambio de conocimientos, estrategias y buenas prácticas entre los establecimientos educativos que conformen parejas de acompañamiento.</li>
	<li>Promover el acompañamiento pedagógico entre directivos docentes y docentes de colegios privados o públicos de alto desempeño e IE oficiales de desempeño medio.</li>
	<li>Contribuir al logro de cambios en la cultura institucional de IE acompañadas, con base en la experiencia compartida por los colegios mentores.</li>
	<li>Apoyar a las IE acompañadas para que mejoren el uso pedagógico de la información institucional.</li>
	<li>Promover la implementación de estrategias didácticas para el desarrollo pedagógico y para la evaluación formativa en las áreas de lenguaje, matemáticas y ciencias naturales.</li>
</ul>
